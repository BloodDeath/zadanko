public class Owoc {
    double waga;
    double cena;
    public Owoc(double waga, double cena)
    {
        this.waga = waga;
        this.cena = cena;
    }
    public Owoc() {}
   public double podajCeneZaKg()
    {
        return cena;
    }
    public double podajWage()
    {
        return waga;
    }


}
